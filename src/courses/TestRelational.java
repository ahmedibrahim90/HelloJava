package courses;

public class TestRelational {

	public static void main(String[] args) {

		int x = 20, y = 20;
		
		//Equal operator
		System.out.print("Is x equal y? " + (x==y));
		
		//Not Equal operator
				System.out.println("Is x Not equal y " + (x!=y));
				
				//Greater than  operator
				System.out.println("Is x Greater than y " + (x>y));
				
				//Greater than or Equal operator
				System.out.println("Is x Greater than or Equal y " + (x>=y));
				
				//Less than operator
				System.out.println("Is x Less than y " + (x<y));
				
				//Less than or Equal operator
				System.out.println("Is x Less than or Equal y " + (x<=y));
	}

}
